    @picker = colored ? NodeColors.new : DefaultColors.new
  end
  def color(index, ingress)
    @picker.color(index, ingress)
  end
  class NodeColors
    def color(index, ingress)
      ColorPicker::COLORS[index % ColorPicker::COLORS.length]
    end
  end
  class DefaultColors
    def color(index, ingress)
      ingress ? :blue : :red
    end
